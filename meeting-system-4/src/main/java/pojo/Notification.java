package pojo;

public class Notification {
    public String getSubForum() {
        return subForum;
    }

    public void setSubForum(String subForum) {
        this.subForum = subForum;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    String subForum;
    String message;
}
